    private void jButtonGerarRoteiroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGerarRoteiroActionPerformed
        
        Aeroporto origem = JFramePrincipal.plano.getAeroporto(this.jTextFieldOrigem.getText());
        Aeroporto destino = JFramePrincipal.plano.getAeroporto(this.jTextFieldDestino.getText());
        
        List<Voo> menorCaminho = JFramePrincipal.plano.getMenorCaminho(origem, destino);
        
        int[] tempoDeVoo = Manipulador.getMinInTime(menorCaminho.get(menorCaminho.size()-1).getAeroportoChegada().getDistancia());
        
        this.jTextAreaRoteiro.setText("");
        this.jTextAreaRoteiro.append(String.format("Tempo de viagem = %02d:%02d\n", tempoDeVoo[0], tempoDeVoo[1]));
        menorCaminho.forEach(voo -> {
            this.jTextAreaRoteiro.append(String.format(
                    "(%s) %s -> %s\n",
                    voo.getCodVoo(),
                    voo.getAeroportoSaida(),
                    voo.getAeroportoChegada()
            ));
        });
        
    }//GEN-LAST:event_jButtonGerarRoteiroActionPerformed
    
    

