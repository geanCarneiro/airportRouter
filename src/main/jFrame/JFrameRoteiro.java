    private void jTextFieldOrigemFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextFieldOrigemFocusLost
        
        String value = this.jTextFieldOrigem.getText().toUpperCase();
        
        if(!value.isEmpty()){
            List<Aeroporto> possibilidades = JFramePrincipal.plano.getAllAeroportosWith(value);
            
            switch(possibilidades.size()){
                case 0:
                    JOptionPane.showMessageDialog(null, "Aeroporto " + value + " inexistente", "ATENÇÃO", JOptionPane.WARNING_MESSAGE);
                    break;
                case 1:
                    this.jTextFieldOrigem.setText(possibilidades.get(0).toString());
                    break;
                default:
                    abrirSelecionarVoo(possibilidades, this.jTextFieldOrigem);
            }
        }
        
    }//GEN-LAST:event_jTextFieldOrigemFocusLost

