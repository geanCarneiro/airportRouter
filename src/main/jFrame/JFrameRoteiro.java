    private void abrirSelecionarVoo(List<Aeroporto> possibilidades, JTextField textField) {
        
        if(selecionar == null)
            selecionar = new JFrameSelecionarAero(possibilidades, textField);
        else{
            selecionar.setPossibilidades(possibilidades);
            selecionar.setOutput(textField);
        }
        selecionar.setVisible(true);
        
    }
